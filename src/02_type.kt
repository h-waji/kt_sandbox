

fun main() {
  // String, Char
  val msg: String = "Hoge"
  println(msg)

  val c: Char = 'a'
  println(c)

  // Byte, Short, Int, Long
  val i: Int = 2023
  println(i.toString())
  val l: Long = 9999999999L
  println(l.toString())

  // Float, Double
  val d: Double = 20.23
  val f: Float = 123.45F
  println(d.toString())
  println(f)

  // Boolean
  val flag: Boolean = false
  println(flag)
}